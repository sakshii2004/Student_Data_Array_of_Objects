package assignment3;

public class Student {
	private String name;
	private int prn;
	private String dob;
	
	// Setter and Getter Methods Creation
	//Name
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return this.name;
	}
	
	//PRN
	public void setPRN(int prn) {
		this.prn = prn;
	}
	
	public int getPRN() {
		return this.prn;
	}
	
	//DOB
	public void setDOB(String dob) {
		this.dob = dob;
	}
	
	public String getDOB() {
		return this.dob;
	}
}
